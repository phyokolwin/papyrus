package InterfaceExample;

public interface MathOperation {

	/**
	 * 
	 * @param num2 
	 * @param num1 
	 */
	public void sumTwoNumbers(int num2, int num1);

	/**
	 * 
	 * @param num3 
	 * @param num2 
	 * @param num1 
	 */
	public void multiplyThreeNumbers(int num3, int num2, int num1);

	/**
	 * 
	 * @param num2 
	 * @param num1 
	 */
	public void sumTwoNumbers(double num2, double num1); 

}
